data_list = [1,2,3,4,5,6,7,8,9]

def balikPosisi(data_list):
    length = len(data_list)
    s = length

    new_list = [None]*length

    for item in data_list:
        s = s - 1
        new_list[s] = item
    return new_list



print(balikPosisi(data_list))

